#include "../so_long.h"

void	find_exit(char **map, int pos[2])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (map[i])
	{
		while (map[i][j])
		{
			if (map[i][j] == 'E')
			{
				pos[1] = i;
				pos[0] = j;
				break ;
			}
			j++;
		}
		j = 0;
		i++;
	}
}
