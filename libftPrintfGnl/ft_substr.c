#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	unsigned int	i;
	char			*str;

	if (!s)
		return (0);
	if (start > (unsigned int)ft_strlen((char *)s))
		return (ft_strdup(""));
	if (len > (size_t)ft_strlen((char *)s + start))
		len = ft_strlen((char *)s + start);
	str = ft_calloc(len + 1, sizeof(char));
	if (!str)
		return (0);
	i = 0;
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	return (str);
}
